<script>
    // Lưu ID section hiện tại
    let currentId = '';

    const sections = document.querySelectorAll('.terms-section');
    const tocLinks = document.querySelectorAll('.toc-list a');

    // Cập nhật currentId khi scroll
    function updateCurrentId() {
        let closestSection = null;
        let closestDistance = Infinity;

        sections.forEach(section => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.offsetHeight;
            const sectionCenter = sectionTop + sectionHeight / 2;

            const viewportCenter = pageYOffset + window.innerHeight / 2;
            const distance = Math.abs(sectionCenter - viewportCenter);

            if (distance < closestDistance) {
                closestDistance = distance;
                closestSection = section.getAttribute('id');
            }
        });

        // Cập nhật currentId
        currentId = closestSection;

        // Highlight link TOC
        tocLinks.forEach(link => {
            link.classList.remove('active');
            if (link.getAttribute('href').slice(1) === currentId) {
                link.classList.add('active');
            }
        });
    }

    // Khi click vào TOC link
    tocLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const sectionId = link.getAttribute('href').slice(1);
            currentId = sectionId;

            // Scroll đến section
            const targetSection = document.getElementById(sectionId);
            if (targetSection) {
                targetSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }

            // Cập nhật highlight
            tocLinks.forEach(l => l.classList.remove('active'));
            link.classList.add('active');
        });
    });

    // Lắng nghe sự kiện scroll
    window.addEventListener('scroll', updateCurrentId);

    // Gọi lần đầu khi load trang
    updateCurrentId();
</script>
